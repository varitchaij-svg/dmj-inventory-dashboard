// tests/bahttext.test.js — จำนวนเงินบาทเป็นตัวอักษร (ใบกำกับภาษี PosReceipt)
import { describe, it, expect } from 'vitest';
import { bahtText } from './helpers.js';

describe('bahtText', () => {
  it('0 → ศูนย์บาทถ้วน', () => expect(bahtText(0)).toBe('ศูนย์บาทถ้วน'));
  it('1 → หนึ่งบาทถ้วน', () => expect(bahtText(1)).toBe('หนึ่งบาทถ้วน'));
  it('11 → สิบเอ็ดบาทถ้วน', () => expect(bahtText(11)).toBe('สิบเอ็ดบาทถ้วน'));
  it('21 → ยี่สิบเอ็ดบาทถ้วน', () => expect(bahtText(21)).toBe('ยี่สิบเอ็ดบาทถ้วน'));
  it('100 → หนึ่งร้อยบาทถ้วน', () => expect(bahtText(100)).toBe('หนึ่งร้อยบาทถ้วน'));
  it('101 → หนึ่งร้อยเอ็ดบาทถ้วน', () => expect(bahtText(101)).toBe('หนึ่งร้อยเอ็ดบาทถ้วน'));
  it('408 → สี่ร้อยแปดบาทถ้วน', () => expect(bahtText(408)).toBe('สี่ร้อยแปดบาทถ้วน'));
  it('2268 (ตัวอย่างจากใบจริง) → สองพันสองร้อยหกสิบแปดบาทถ้วน', () =>
    expect(bahtText(2268)).toBe('สองพันสองร้อยหกสิบแปดบาทถ้วน'));
  it('1000000 → หนึ่งล้านบาทถ้วน', () => expect(bahtText(1000000)).toBe('หนึ่งล้านบาทถ้วน'));
  it('1234567 → หนึ่งล้านสองแสนสามหมื่นสี่พันห้าร้อยหกสิบเจ็ดบาทถ้วน', () =>
    expect(bahtText(1234567)).toBe('หนึ่งล้านสองแสนสามหมื่นสี่พันห้าร้อยหกสิบเจ็ดบาทถ้วน'));
  it('มีสตางค์: 25.50 → ยี่สิบห้าบาทห้าสิบสตางค์', () =>
    expect(bahtText(25.5)).toBe('ยี่สิบห้าบาทห้าสิบสตางค์'));
  it('เศษสตางค์ปัดเป็นถ้วน: 148.37 → หนึ่งร้อยสี่สิบแปดบาทสามสิบเจ็ดสตางค์', () =>
    expect(bahtText(148.37)).toBe('หนึ่งร้อยสี่สิบแปดบาทสามสิบเจ็ดสตางค์'));
  it('ปัดเศษ .005 ขึ้น: 0.01 → หนึ่งสตางค์', () => expect(bahtText(0.01)).toBe('หนึ่งสตางค์'));
});
